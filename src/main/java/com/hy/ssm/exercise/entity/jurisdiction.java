package com.hy.ssm.exercise.entity;

public class jurisdiction {
    private Integer j_id;
    private String j_name;
    private Integer j_toto;
    private String j_url;

    public Integer getJ_id() {
        return j_id;
    }

    public void setJ_id(Integer j_id) {
        this.j_id = j_id;
    }

    public String getJ_name() {
        return j_name;
    }

    public void setJ_name(String j_name) {
        this.j_name = j_name;
    }

    public Integer getJ_toto() {
        return j_toto;
    }

    public void setJ_toto(Integer j_toto) {
        this.j_toto = j_toto;
    }

    public String getJ_url() {
        return j_url;
    }

    public void setJ_url(String j_url) {
        this.j_url = j_url;
    }
}
